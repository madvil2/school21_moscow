char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < n || *src != '\0')
	{
		if (i < n && *src != '\0')
		{
			*dest++ = *src++;
		}
		else
			*dest++ = '\0';
		i++;
	}
	return (dest);
}
