char	*ft_rot42(char *str)
{
	char	*param;
	int		i;

	i = 0;
	while (*str != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			param[i] = ((str[i] - 97) + 16) % 26 + 97;
			i++;
		}
		else if (str[i] >= 65 && str[i] <= 90)
		{
			param[i] = ((str[i] - 65) + 16) % 26 + 65;
			i++;
		}
	}
}
